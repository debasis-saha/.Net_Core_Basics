using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using Crud_Operation_Logging.Models;

namespace Crud_Operation_Logging.Pages.ProductInfo
{
    public class DetailsModel : PageModel
    {
        private readonly Crud_Operation_Logging.Models.ProductDataContext _context;

        public DetailsModel(Crud_Operation_Logging.Models.ProductDataContext context)
        {
            _context = context;
        }

        public Product Product { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            Product = await _context.Product.FirstOrDefaultAsync(m => m.Id == id);

            if (Product == null)
            {
                return NotFound();
            }
            return Page();
        }
    }
}
